      <h2 class="subtitulos">EN QUE CONSISTE</h2>
        <P>Es una plataforma web para <strong> la comercialización de productos campesinos</strong>, de  esta forma podrán vender sus productos con mayor  facilidad y sin necesidad de intermediarios.
Atreves de esta plataforma se verán beneﬁciados  tanto productores, comerciantes y transportadores de  alimentos ya que los campesinos podrán ofrecer sus  productos a cualquier parte del país y cualquier  comerciante podrá ver los productos que están  disponibles en el momento y hacer sus pedidos si lo  desean.
</P>

<h3 class="subtitulos">¿COMO REGISTRARSE?</h3>
  <P class="parrafo2">se puede registrar bajo tres perfiles:  </P>
  <div class="centrado"><img  src="views/imagenes/perfiles.jpg" alt=""></div>
  <p class="parrafo2"><strong> Productores </strong>: pueden registrar dueños de fincas que esten interesados en vender sus productos.</p>  
  <p class="parrafo2"><strong>Comerciantes </strong>: bajo este perfil se registran dueños de negocios que esten interesados en adquirir productos campesinos con una mayor facilidad ya que estan negociando directamente con los productores.</p>  
  <p class="parrafo2"><strong>Transportadores</strong>:se registran dueños de vehiculos que esten interesados en transportar los productos a los diferentes puntos acordados para la entrega. </p>
